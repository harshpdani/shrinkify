import mongoose from 'mongoose';

const LinkSchema = new mongoose.Schema({
    originalUrl: {
        type: String,
        required: true,
    },
    shortCode: {
        type: String,
        required: true,
        unique: true,
    },
    customText: {
        type: String,
        unique: true,
        sparse: true,
    },
    clicks: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

export default mongoose.models.Link || mongoose.model("Link", LinkSchema);